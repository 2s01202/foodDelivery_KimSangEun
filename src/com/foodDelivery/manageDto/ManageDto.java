package com.foodDelivery.manageDto;

public class ManageDto {
	private int manager_num;
	private String manager_id; 
	private String manager_password; 
	private String manager_nickname;
	
	
	public int getManager_num() {
		return manager_num;
	}
	public void setManager_num(int manager_num) {
		this.manager_num = manager_num;
	}
	public String getManager_id() {
		return manager_id;
	}
	public void setManager_id(String manager_id) {
		this.manager_id = manager_id;
	}
	public String getManager_password() {
		return manager_password;
	}
	public void setManager_password(String manager_password) {
		this.manager_password = manager_password;
	}
	public String getManager_nickname() {
		return manager_nickname;
	}
	public void setManager_nickname(String manager_nickname) {
		this.manager_nickname = manager_nickname;
	} 
	
	
}
