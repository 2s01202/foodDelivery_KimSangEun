package com.foodDelivery.marketDto;

public class MarketDto {
	
	private int market_num;
	private String market_name; 
	private String market_cate; 
	private String market_phone; 
	private String market_time; 
	private String market_intro; 
	private String market_address; 
	private String market_img;
	
	
	public int getMarket_num() {
		return market_num;
	}
	public void setMarket_num(int market_num) {
		this.market_num = market_num;
	}
	public String getMarket_name() {
		return market_name;
	}
	public void setMarket_name(String market_name) {
		this.market_name = market_name;
	}
	public String getMarket_cate() {
		return market_cate;
	}
	public void setMarket_cate(String market_cate) {
		this.market_cate = market_cate;
	}
	public String getMarket_phone() {
		return market_phone;
	}
	public void setMarket_phone(String market_phone) {
		this.market_phone = market_phone;
	}
	public String getMarket_time() {
		return market_time;
	}
	public void setMarket_time(String market_time) {
		this.market_time = market_time;
	}
	public String getMarket_intro() {
		return market_intro;
	}
	public void setMarket_intro(String market_intro) {
		this.market_intro = market_intro;
	}
	public String getMarket_address() {
		return market_address;
	}
	public void setMarket_address(String market_address) {
		this.market_address = market_address;
	}
	public String getMarket_img() {
		return market_img;
	}
	public void setMarket_img(String market_img) {
		this.market_img = market_img;
	} 
	
	
	
}
